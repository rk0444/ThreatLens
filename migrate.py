def run_backfill(db_path):
    print(f"\nRunning risk score backfill on existing rows...")
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()

    cursor.execute("SELECT COUNT(*) FROM cves WHERE risk_score = 0")
    count = cursor.fetchone()[0]
    print(f"  Rows with risk_score = 0: {count}")

    if count == 0:
        print("  Nothing to backfill.")
        conn.close()
        return

    # SQLite UPDATE with CASE referencing the just-set column is evaluated row by row
    # Split into two passes for correctness
    cursor.executescript("""
        UPDATE cves
        SET risk_score = ROUND(
            (COALESCE(cvss_score, 0) / 10.0 * 25)
            + (COALESCE(epss_score, 0) * 20)
            + (CASE WHEN cisa_kev = 1 THEN 15 ELSE 0 END)
            + (CASE WHEN actively_exploited = 1 THEN 10 ELSE 0 END)
            + (CASE WHEN asset_affected = 1 THEN 10 ELSE 0 END)
            + (CASE WHEN ransomware_use = 1 THEN 5 ELSE 0 END)
            + (CASE WHEN patch_available = 0 THEN 5 ELSE 0 END)
            + (
                CASE
                    WHEN published_date >= date('now', '-1 day')  THEN 10
                    WHEN published_date >= date('now', '-3 days') THEN 7
                    WHEN published_date >= date('now', '-7 days') THEN 4
                    ELSE 0
                END
            ), 2)
        WHERE risk_score = 0;

        UPDATE cves
        SET risk_band = CASE
            WHEN risk_score >= 86 THEN 'CRITICAL'
            WHEN risk_score >= 61 THEN 'HIGH'
            WHEN risk_score >= 31 THEN 'MEDIUM'
            ELSE 'LOW'
        END;
    """)

    conn.commit()

    cursor.execute("SELECT risk_band, COUNT(*) FROM cves GROUP BY risk_band ORDER BY COUNT(*) DESC")
    rows = cursor.fetchall()
    print("  Backfill complete. Distribution:")
    for band, cnt in rows:
        print(f"    {band:<10} {cnt} CVEs")

    conn.close()


# ── Entry point ───────────────────────────────────────────────────────────────

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="ThreatLens CVE migration")
    parser.add_argument("--db",      default=DEFAULT_DB, help="Path to threatlens.sqlite")
    parser.add_argument("--dry-run", action="store_true", help="Preview changes without writing")
    parser.add_argument("--backfill",action="store_true", help="Also backfill risk scores on existing rows")
    args = parser.parse_args()

    run_migration(args.db, dry_run=args.dry_run)

    if args.backfill and not args.dry_run:
        run_backfill(args.db)
